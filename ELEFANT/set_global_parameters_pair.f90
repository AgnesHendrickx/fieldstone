subroutine set_global_parameters_pair

use global_parameters

implicit none

!==================================================================================================!
!==================================================================================================!
!@@ \subsubsection{set\_global\_parameters\_pair}
!@@
!==================================================================================================!

if (iproc==0) then

if (pair=='q1p0') then 
   mV=2**ndim
   mP=1
   mT=2**ndim
   if (ndim==2) then
      nel=nelx*nely
      NV=(nelx+1)*(nely+1)
      NT=(nelx+1)*(nely+1)
      NP=nel
   else
      nel=nelx*nely*nelz
      NV=(nelx+1)*(nely+1)*(nelz+1)
      NT=(nelx+1)*(nely+1)*(nelz+1)
      NP=nel
   end if
end if

if (pair=='q1q1') then
   mP=2**ndim
   mT=2**ndim
   if (ndim==2) then
      mV=2**ndim+1
      nel=nelx*nely
      NV=(nelx+1)*(nely+1)+nel
      NT=(nelx+1)*(nely+1)
      NP=(nelx+1)*(nely+1)
   else
      mV=2**ndim+2
      nel=nelx*nely*nelz
      NV=(nelx+1)*(nely+1)*(nelz+1)+2*nel
      NT=(nelx+1)*(nely+1)*(nelz+1)
      NP=(nelx+1)*(nely+1)*(nelz+1)
   end if
end if

write(*,'(a)') '     >> set_global_parameters_pair '

end if ! iproc

end subroutine

